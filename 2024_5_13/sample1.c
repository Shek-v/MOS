#include <stdio.h>
/*
 *   Sample program for arithmetic operators
 */
 
void main()
{
    // Calculation
    printf("%d + %d = %d\n", 5, 2, 5+2);		/* Add */
    printf("%d - %d = %d\n", 5, 2, 5-2);		/* Subtract */
    printf("%d * %d = %d\n", 5, 2, 5*2);		/* Multiply */
    printf("%d / %d = %d Modulo %d\n", 5, 2, 5/2, 5%2);	/* Divide */  
}
